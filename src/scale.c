#include "fdf.h"
#include <math.h>

void	scale(t_fdf fdf, t_point p)
{
	p->position.x *= (1.0 / 3.0) * 1920.0 / (t_f64)(fdf->map->width - 1);
	p->position.y *= (1.0 / 3.0) * 1080.0 / (t_f64)(fdf->map->height - 1);
}
